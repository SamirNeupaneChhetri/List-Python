# append() method
list = ["apple", "banana", "cherry"]
list.append("orange") # To add an item to the end of the list,
print(list)

# insert() method
thislist = ["apple", "banana", "cherry"]
thislist.insert(1, "orange") # To insert a list item at a specified index,
print(thislist)


# extend() method.
thisl = ["apple", "banana", "cherry"]
tropical = ["mango", "pineapple", "papaya"]
thisl.extend(tropical) # Add the elements of tropical to thisl:
print(thisl)


# extend() method 
list1 = ["apple", "banana", "cherry"]
thistuple = ("kiwi", "orange")
list1.extend(thistuple) # you can add any iterable object (tuples, sets, dictionaries etc. in list).
print(list1)




